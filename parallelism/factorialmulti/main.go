package main

import "fmt"

func main() {
	nums := gen()
	f := factorial(nums)
	for n := range f {
		fmt.Println(n)
	}
}

// Generator for numbers to test
func gen() <-chan int {
	out := make(chan int)
	go func() {
		for i := 0; i < 10; i++ {
			for j := 3; j < 13; j++ {
				out <- j
			}
		}
		close(out)
	}()
	return out
}

// Runs the factorial calculation in parrallel on each member of the input channel
func factorial(in <-chan int) <-chan int {
	out := make(chan int)
	go func() {
		for n := range in {
			out <- fact(n)
		}
		close(out)
	}()
	return out
}

func fact(n int) int {
	total := 1
	for i := n; i > 0; i-- {
		total *= i
	}
	return total
}
